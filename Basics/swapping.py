# For example

a = 3
b = 4

a, b = b, a

# The above code line let's break it down...
# at the left side we've 2 variables (a & b) which we already assigned values to it...
# so at the right side we're swapping the 2 values, meaning that value of 'b' will be assigned to a and value of 'a' will be assigned to b.

